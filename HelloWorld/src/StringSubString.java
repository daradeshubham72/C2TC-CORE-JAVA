package com.cg.Strings;

public class StringSubString { 
public static void main(String[] args){
{
	String s=new String("HELLO FRIENDS");//Create string
	
	System.out.println(s.substring(0)); //begin index--->start index will be included
	                                   //first letter included
	
	System.out.println(s.substring(3,9));//begin index--->start index include
	                                    //but end index will be exclude
	                                   //1st letter include,last letter exclude(-

}

}

}

