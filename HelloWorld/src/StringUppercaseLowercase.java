package com.cg.Strings;

public class StringUppercaseLowercase {

	public static void main(String[] args) {
		
		String s=new String("Hello Friends");//Create string
		String lower=s.toLowerCase(); //string to lower case
        System.out.println(lower); // print 
        
        String upper=s.toUpperCase();//string to upper
 
        System.out.println(upper);//print
        

        
	}

}
