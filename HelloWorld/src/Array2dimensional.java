package com.cg.Arrays;

public class Array2dimensional {

	public static void main(String[] args) {
		
		int [][] x= {{23,45},{12,43},{34,54},{46,75}};
		
		System.out.println(x);//Address
		
		System.out.println(x[0][1]);//Address
		
		System.out.println(x[1][0]);//Address
		
		System.out.println(x[3][1]);//Address
		
		System.out.println(x[2][0]);//Address

	}

}
